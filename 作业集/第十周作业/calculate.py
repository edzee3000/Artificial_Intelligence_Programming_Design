

def add(a,b):
    '''返回两数之和'''
    return a+b
def subtract(a,b):
    '''返回两数之差'''
    return a-b
def multiply(a,b):
    '''返回两数之积'''
    return a*b
def divide(a,b):
    '''返回两数之商'''
    return a/b
if __name__=="__main__":
    print("3+4的结果为：",add(3,4))
    print("3-4的结果为：",subtract(3,4))
    print("3*4的结果为：",multiply(3,4))
    print("3/4的结果为：",divide(3,4))
